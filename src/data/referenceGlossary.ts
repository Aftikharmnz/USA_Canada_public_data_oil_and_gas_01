function searchableEntry(entry: ReferenceGlossaryEntry): string {
  return [
    entry.term,
    ...entry.aliases,
    entry.productFamily,
    entry.concept,
    entry.plainLanguage,
    entry.officialDefinition,
    entry.traderInterpretation,
    ...entry.inclusions,
    ...entry.exclusions,
  ].join(" ").toLocaleLowerCase();
}

export function filterReferenceGlossary(
  entries: ReferenceGlossaryEntry[],
  query: string,
  productFamily: ReferenceFilterValue<ReferenceProductFamily>,
  concept: ReferenceFilterValue<ReferenceConcept>,
): ReferenceGlossaryEntry[] {
  const normalizedQuery = query.trim().toLocaleLowerCase();

  return entries.filter((entry) => {
    if (productFamily !== "all" && entry.productFamily !== productFamily) return false;
    if (concept !== "all" && entry.concept !== concept) return false;
    return normalizedQuery.length === 0 || searchableEntry(entry).includes(normalizedQuery);
  });
}
